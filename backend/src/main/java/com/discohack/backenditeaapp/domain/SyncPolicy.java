package com.discohack.backenditeaapp.domain;


public enum SyncPolicy {

    /**
     * ALWAYS — всегда синхронизировать немедленно.
     *
     * При подключении аккаунта: все файлы с этой политикой сразу скачиваются в кеш.
     * При изменении локального файла: сразу заливается в облако.
     * При изменении в облаке: сразу скачивается.
     *
     * Используется для: рабочих документов, кода, важных файлов.
     * Минус: занимает место в кеше, требует постоянного интернета.
     */
    ALWAYS("Всегда синхронизировать"),

    /**
     * ON_DEMAND — скачивать только при обращении (дефолт).
     *
     * Файл появляется в Nautilus как обычный файл.
     * Но на диске его нет — только placeholder-метаданные.
     * При открытии файла (read() в FUSE) — скачивается с облака.
     *
     * Используется для: медиафайлов, архивов, старых документов.
     * Плюс: экономит место. Минус: первое открытие медленное.
     */
    ON_DEMAND("По запросу"),

    /**
     * MANUAL — только ручная синхронизация.
     *
     * Файл вообще не скачивается автоматически.
     * При попытке открыть через FUSE возвращается специальная ошибка
     * (или placeholder-файл с объяснением).
     * Скачать можно только явно через UI приложения.
     *
     * Используется для: бэкапов, архивов, очень больших файлов.
     */
    MANUAL("Только вручную"),

    /**
     * SCHEDULED — синхронизация по расписанию (cron).
     *
     * Файлы синхронизируются не сразу, а по заданному cron-расписанию.
     * Например: каждую ночь в 3:00, каждый час, раз в неделю.
     * Cron-выражение хранится в SyncRule.cronExpression.
     *
     * Используется для: ежедневных бэкапов, еженедельных архивов.
     */
    SCHEDULED("По расписанию");

    private final String displayName;

    SyncPolicy(String displayName) {
        this.displayName = displayName;
    }

    public String getDisplayName() {
        return displayName;
    }
}
